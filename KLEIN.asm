.endmacro

.macro rotate_sub_mix_nibbles
	; Arguments: the dest (@0-@7) and source (@8-@15) registers for the state
	; Combines the RotateNibbles, SubNibbles and MixNibbles steps
	
	; Start with matrix 1 (left half of the state)
	; Load mult2 * four leftmost state bytes
	ldi r31, high(sboxmult2 * 2)

	mov r30, @10
	lpm @0, Z

	mov r30, @11
	lpm @1, Z

	mov r30, @12
	lpm @2, Z

	mov r30, @13
	lpm @3, Z

	; Load mult1 * four leftmost state bytes
	ldi r31, high(sboxmult1 * 2)
	
	; mov r30, @13 [already done in mult2-phase]
	lpm r24, Z
	eor @0, r24
	eor @1, r24
	
	mov r30, @12
	lpm r24, Z
	eor @0, r24
	eor @3, r24
	
	mov r30, @11
	lpm r24, Z
	eor @2, r24
	eor @3, r24
	
	mov r30, @10
	lpm r24, Z
	eor @1, r24
	eor @2, r24
	
	; Load mult3 * four leftmost state bytes
	ldi r31, high(sboxmult3 * 2)

	; mov r30, @10 [already done in mult1-phase]
	lpm r24, Z
	eor @3, r24
	
	mov r30, @11
	lpm r24, Z
	eor @0, r24
	
	mov r30, @12
	lpm r24, Z
	eor @1, r24
	
	mov r30, @13 
	lpm r24, Z
	eor @2, r24

	; Start with matrix 2 (right half of the state)
	; Load mult3 * four rightmost state bytes
	; ldi r31, high(sboxmult3 * 2) [already done in previous mult3-phase]
	
	mov r30, @15
	lpm @4, Z

	mov r30, @8
	lpm @5, Z

	mov r30, @9
	lpm @6, Z
	
	mov r30, @14
	lpm @7, Z

	; Load mult2 * four rightmost state bytes
	ldi r31, high(sboxmult2 * 2)

	; mov r30, @14 [already done in mult3-phase]
	lpm r24, Z
	eor @4, r24

	mov r30, @9
	lpm r24, Z
	eor @7, r24

	mov r30, @8
	lpm r24, Z
	eor @6, r24

	mov r30, @15
	lpm r24, Z
	eor @5, r24
	
	; Load mult1 * four rightmost state bytes
	ldi r31, high(sboxmult1 * 2)
	
	; mov r30, @15 [already done in mult2-phase]
	lpm r24, Z
	eor @6, r24
	eor @7, r24
	
	mov r30, @8
	lpm r24, Z
	eor @4, r24
	eor @7, r24
	
	mov r30, @9
	lpm r24, Z
	eor @4, r24
	eor @5, r24

	mov r30, @14
	lpm r24, Z
	eor @6, r24
	eor @5, r24
.endmacro

.macro rotate_sub_mix_nibbles_high2low
	; Assumes the state is currently in r16-r23, leaves it r8-r15 afterwards
	rotate_sub_mix_nibbles r8, r9, r10, r11, r12, r13, r14, r15, r16, r17, r18, r19, r20, r21, r22, r23
.endmacro

.macro rotate_sub_mix_nibbles_low2high
