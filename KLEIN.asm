	rotate_sub_mix_nibbles r16, r17, r18, r19, r20, r21, r22, r23, r8, r9, r10, r11, r12, r13, r14, r15 
.endmacro

.macro keyschedule
	; Arguments: the registers in which the key is stored, and the round number
	; Schedules a new round key
	
	; Xor the right half into the left half (rotate implicitly)
	eor @1, @5
	eor @2, @6
	eor @3, @7
	eor @0, @4

	; Add the round number to the new sk7
	ldi r24, @8
	eor @7, r24

	; Run new sk5 and sk6 through sbox
	; ldi r31, high(sbox *2) not necessary; already done in mix of previous round
	
	mov r30, @2
	lpm @2, Z

	mov r30, @3
	lpm @3, Z
.endmacro

