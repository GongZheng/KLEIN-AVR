.macro add_roundkey_low
	; Arguments: the registers in which the key is stored
	; Adds the round key to the state, assuming the state is in r8-r15 ("low")
	eor r8, @0
	eor r9, @1
	eor r10, @2
	eor r11, @3
	eor r12, @4
	eor r13, @5
	eor r14, @6
	eor r15, @7
.endmacro

